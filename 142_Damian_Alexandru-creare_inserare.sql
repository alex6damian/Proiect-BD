CREATE TABLE LOCATIE
(
  LOCATIE_ID INT PRIMARY KEY,
  LOCATIE_NUME VARCHAR(50),
  LOCATIE_ADRESA VARCHAR(50)
);

CREATE TABLE CASINO
(
  CASINO_ID INT PRIMARY KEY,
  CASINO_NUME VARCHAR(50),
  LOCATIE_ID INT,
    FOREIGN KEY (LOCATIE_ID) REFERENCES LOCATIE(LOCATIE_ID)
);

CREATE TABLE STAFF(
    STAFF_ID INT PRIMARY KEY,
    STAFF_NUME VARCHAR(50),
    STAFF_PRENUME VARCHAR(50),
    STAFF_SALARIU DECIMAL(10,2),
    STAFF_DATA_ANGAJARE DATE,
    CASINO_ID INT,
    FOREIGN KEY (CASINO_ID) REFERENCES CASINO(CASINO_ID)
);

CREATE TABLE RESPONSABIL_JOC(
    STAFF_ID INT,
    JOC_ID INT,
    PRIMARY KEY (STAFF_ID, JOC_ID),
    FOREIGN KEY (STAFF_ID) REFERENCES STAFF(STAFF_ID),
    FOREIGN KEY (JOC_ID) REFERENCES JOC(JOC_ID)
);

CREATE TABLE CASIER(
    STAFF_ID INT,
    PRIMARY KEY (STAFF_ID),
    FOREIGN KEY (STAFF_ID) REFERENCES STAFF(STAFF_ID)
);

CREATE TABLE JUCATOR(
    JUCATOR_ID INT PRIMARY KEY,
    JUCATOR_NUME VARCHAR(50),
    JUCATOR_PRENUME VARCHAR(50),
    JUCATOR_DATA_NASTERE DATE,
    JUCATOR_ADRESA VARCHAR(50),
    JUCATOR_EMAIL VARCHAR(50),
    JUCATOR_TELEFON VARCHAR(50)
);

CREATE TABLE TRANZACTIE(
    TRANZACTIE_ID INT PRIMARY KEY,
    TRANZACTIE_DATA DATE,
    TRANZACTIE_SUMA DECIMAL(10,2),
    JUCATOR_ID INT,
    STAFF_ID INT,
    FOREIGN KEY (JUCATOR_ID) REFERENCES JUCATOR(JUCATOR_ID),
    FOREIGN KEY (STAFF_ID) REFERENCES CASIER(STAFF_ID)
);

CREATE TABLE PARIU_JOC(
    JUCATOR_ID INT,
    JOC_ID INT,
    PARIU_SUMA DECIMAL(10,2),
    PRIMARY KEY (JUCATOR_ID, JOC_ID),
    FOREIGN KEY (JUCATOR_ID) REFERENCES JUCATOR(JUCATOR_ID),
    FOREIGN KEY (JOC_ID) REFERENCES JOC(JOC_ID)
);

CREATE TABLE ZONA(
    ZONA_ID INT PRIMARY KEY,
    ZONA_NUME VARCHAR(50)
);

CREATE TABLE JOC(
    JOC_ID INT PRIMARY KEY,
    ZONA_ID INT,
    JOC_NUME VARCHAR(50),
    JOC_TIP VARCHAR(50),
    JOC_PRET DECIMAL(10,2),
    CASINO_ID INT,
    FOREIGN KEY (CASINO_ID) REFERENCES CASINO(CASINO_ID),
    FOREIGN KEY (ZONA_ID) REFERENCES ZONA(ZONA_ID)
);

